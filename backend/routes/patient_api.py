# 3. CLINICAL DECISION SUPPORT SYSTEM (CDSS) - EMERGENCY POLLING
# ==============================================================================
@patient_bp.route('/rescue/cdss-status', methods=['GET'])
def get_emergency_protocols():
    """
    Polled by the Patient UI. If risk is CRITICAL, this returns 
    the Immediate First-Aid steps and Ambulance ETA.
    """
    p_id = request.args.get('node_id')
    if p_id not in db.patients: return abort(404, "Invalid Node")
    
    p = db.patients[p_id]
    
    if p['risk_profile'] == "CRITICAL":
        # Extract 'CRITICAL' and 'ACTION' tags from AI Cache
        ai_cache = p.get('ai_advisor_cache', {})
        critical_steps = []
        
        # Handle dict format returning from newer AI Engine
        if isinstance(ai_cache, dict):
            critical_steps.extend(ai_cache.get('warning', []))
            critical_steps.extend(ai_cache.get('action', []))
        # Handle list format from older AI Engine
        elif isinstance(ai_cache, list):
            for instruction in ai_cache:
                if isinstance(instruction, dict) and instruction.get('type') in ['CRITICAL', 'ACTION', 'EMERGENCY']:
                    critical_steps.append(instruction.get('msg', ''))
        
        # Fallback instructions if AI processing delayed
        if not critical_steps:
            critical_steps = ["Maintain airway", "Do not move abruptly", "Wait for emergency unit"]

        return jsonify({
            "status": "CRITICAL_OVERRIDE_ACTIVE",
            "ambulance_eta": "06:42 MIN", # In production, integrate Google Maps API here
            "first_aid_steps": critical_steps,
            "physician_on_call": "Dr. Peer Mohammed"
        })
        
    return jsonify({"status": "MONITORING_STABLE"})

