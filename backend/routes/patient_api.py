# 2. SECURE BIOMETRIC HANDSHAKE (LOGIN)
# ==============================================================================
@patient_bp.route('/auth/handshake', methods=['POST'])
def verify_patient_node():
    """Authenticates the Patient UI using the Node ID generated by the Physician."""
    data = request.json
    p_id = data.get('node_id')
    
    if p_id in db.patients:
        p = db.patients[p_id]
        logger.info(f"Secure Handshake Established for Node: {p_id}")
        return jsonify({
            "status": "AUTHORIZED",
            "patient_name": p['name'],
            "protocol": p['history'],
            "session_token": p.get('security_hash', 'token_xyz')
        })
        
    return jsonify({"error": "NODE_NOT_FOUND", "code": "AUTH_404"}), 404

