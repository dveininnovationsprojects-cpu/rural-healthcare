# 5. PATIENT PDF DOWNLOAD (GET ROUTE)
# ==============================================================================
@patient_bp.route('/prescription/download', methods=['GET'])
def download_patient_rx():
    """Builds and serves the PDF file directly to the patient's browser."""
    pid = request.args.get('node_id')
    p = db.patients.get(pid)
    if not p: return abort(404, "Patient not found")

    buffer = BytesIO()
    pdf = canvas.Canvas(buffer, pagesize=letter)
    pdf.setFont("Helvetica-Bold", 24)
    pdf.drawString(50, 750, "ZHOPINGO MED - CLINICAL Rx")
    
    pdf.setFont("Helvetica", 12)
    pdf.drawString(50, 725, f"Date: {datetime.now().strftime('%Y-%m-%d %H:%M')}")
    pdf.line(50, 715, 550, 715)
    
    pdf.drawString(50, 690, f"PATIENT IDENTITY: {p['name']} | NODE: {pid}")
    pdf.drawString(50, 670, f"DIAGNOSIS: {p['history']}")
    
    pdf.setFont("Helvetica-Bold", 14)
    pdf.drawString(50, 630, "PRESCRIBED CARE PLAN:")
    
    pdf.setFont("Helvetica", 12)
    y_pos = 610
    
    # Get the latest prescription from the vault
    latest_rx = p['prescription_vault'][-1]['meds'] if p['prescription_vault'] else "No prescriptions found. Please consult physician."
    
    # Handle multi-line strings
    for line in latest_rx.split('\n'):
        if line.strip(): # Skip empty lines
            pdf.drawString(70, y_pos, f"• {line.strip()}")
            y_pos -= 20
        
    pdf.save()
    buffer.seek(0)
    
    return send_file(
        buffer, 
        as_attachment=True, 
        download_name=f"Zhopingo_Rx_{pid}.pdf", 
        mimetype='application/pdf'
    )

