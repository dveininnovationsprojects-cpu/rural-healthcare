# Import database and AI engine from your central files
from database import db 
from services.ai_engine import ClinicalIntelligenceMatrix

logger = logging.getLogger("ZHOPINGO_PATIENT_API")
patient_bp = Blueprint('patient_bp', __name__)

# ==============================================================================
# 1. HARDWARE TELEMETRY INGESTION (ESP32 / SENSORS)
# ==============================================================================
@patient_bp.route('/telemetry/ingest', methods=['POST'])
def ingest_hardware_data():
    """
    INDUSTRIAL INGESTION ENDPOINT:
    Receives high-frequency raw data from ESP32/Sensors, processes it through 
    the AI Matrix, and determines the real-time risk profile.
    """
    try:
        data = request.json
        p_id = data.get('node_id')
        
        if p_id not in db.patients:
            logger.warning(f"Telemetry rejected: Unauthorized Node ID {p_id}")
            return jsonify({"error": "UNAUTHORIZED_NODE", "code": "TEL_401"}), 401

        p = db.patients[p_id]
        
        # Raw Data Extraction (Fallback to averages if hardware fails)
        raw_temp = float(data.get('temp', p.get('biometric_averages', {}).get('temp', 36.5)))
        raw_hr = float(data.get('hr', p.get('biometric_averages', {}).get('hr', 75)))
        raw_accel = float(data.get('accel', 1.0))

        # 1. Pass through AI Clinical Intelligence Matrix
        ai_analysis = ClinicalIntelligenceMatrix.process_telemetry(
            p_id, raw_temp, raw_hr, raw_accel, p['age'], p['history']
        )
        
        # 2. Update Patient Cache
        p['ai_advisor_cache'] = ai_analysis
        p['risk_profile'] = ai_analysis['risk_profile']
        
        # 3. Create Telemetry Payload
        telemetry_entry = {
            "time": datetime.now().isoformat(),
            "temp": raw_temp,
            "hr": raw_hr,
            "accel": raw_accel,
            "risk": ai_analysis['risk_profile']
        }
        
        # 4. Append to Vault & Manage Buffer (Max 10000 records per patient)
        p['vitals_history'].append(telemetry_entry)
        if len(p['vitals_history']) > 10000:
            p['vitals_history'].pop(0)

        # 5. Global Audit for Legal Compliance
        if ai_analysis['risk_profile'] == "CRITICAL":
            db.audit_log.append({
                "timestamp": telemetry_entry['time'],
                "node": p_id,
                "event": "CRITICAL_BIOMETRIC_BREACH",
                "severity_score": ai_analysis.get('severity_score', 100)
            })

        # 6. Response Command to Hardware (e.g., Turn on local buzzer)
        hw_command = "TRIGGER_ALARM" if ai_analysis['risk_profile'] == "CRITICAL" else "STANDBY"
        
        return jsonify({
            "status": "SYNC_SUCCESS",
            "risk_profile": ai_analysis['risk_profile'],
            "hardware_command": hw_command
        })

    except Exception as e:
        logger.error(f"Telemetry Ingestion Fault: {str(e)}")
        return jsonify({"error": "INGESTION_FAULT", "details": str(e)}), 500

