@patient_bp.route('/vault/sync', methods=['GET'])
def get_patient_vault():
    """Retrieves all historical data, chats, and prescriptions for the Patient UI."""
    p_id = request.args.get('node_id')
    if p_id not in db.patients: return abort(404, "Invalid Node")
    
    p = db.patients[p_id]
    return jsonify({
        "chat_history": p['chat_vault'][-50:], # Last 50 messages
        "prescriptions": p['prescription_vault'],
        "active_notifications": p['notification_hub']
    })

@patient_bp.route('/chat/send', methods=['POST'])
def send_patient_message():
    """Handles patient sending messages to the physician."""
    data = request.json
    p_id = data.get('node_id')
    text = data.get('message')
    
    if p_id not in db.patients: return abort(404, "Invalid Node")
    if not text: return abort(400, "Message cannot be empty")
    
    msg_obj = {
        "time": datetime.now().strftime("%H:%M"),
        "sender": "Patient",
        "text": text
    }
    db.patients[p_id]['chat_vault'].append(msg_obj)
    
    # Notify Doctor (Optional depending on your UI logic)
    if 'pending_queue' in db.doctors[db.patients[p_id]['doctor_id']]:
        db.doctors[db.patients[p_id]['doctor_id']]['pending_queue'].append(f"NEW_MSG_{p_id}")
    
    return jsonify({"status": "MESSAGE_DELIVERED"})

@patient_bp.route('/clear-notifs', methods=['POST'])
def clear_patient_notifs():
    """Clears notifications once viewed in Patient UI."""
    p_id = request.json.get('p_id')
    if p_id in db.patients:
        db.patients[p_id]['notification_hub'] = []
        return jsonify({"status": "CLEARED"})
    return jsonify({"error": "NOT_FOUND"}), 404

